package com.bm.entity;

public class BaobeiUserinfo {
	private String followupCount;//报备次数
	private String regionName;//城市
	private String accountMoney;//账户余额
	private String nickName;//家长昵称
	private String areaName;//地区
	private String avatar;//家长头像
	private String userId;//家长id
	public String getFollowupCount() {
		return followupCount;
	}
	public void setFollowupCount(String followupCount) {
		this.followupCount = followupCount;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
	public String getAccountMoney() {
		return accountMoney;
	}
	public void setAccountMoney(String accountMoney) {
		this.accountMoney = accountMoney;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getAreaName() {
		return areaName;
	}
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}
	public String getAvatar() {
		return avatar;
	}
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	


}
